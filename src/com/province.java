package com;
public class province {
	private String province;
	private int sum;
	public province(String province,int sum)
	{
		this.province = province;
		this.sum = sum;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	
}
